#!/usr/bin/env julia
#
# Simple benchmark runner for current state only (no comparison)
# Creates a markdown table showing benchmark results
#

using AirspeedVelocity
using Dates
using Pkg: PackageSpec

const PKG_NAME = "MacroEconometricTools"
const REPO_ROOT = abspath(joinpath(@__DIR__, ".."))
const BENCH_SCRIPT = joinpath(@__DIR__, "benchmarks.jl")
const BENCH_PROJECT = joinpath(@__DIR__, "Project.toml")
const OUTPUT_DIR = abspath(joinpath(@__DIR__, "results"))

# Benchmark only the current dirty state
const CURRENT_REV = "dirty"

mkpath(OUTPUT_DIR)

println("=" ^ 60)
println("Benchmarking current state of MacroEconometricTools")
println("=" ^ 60)
println("Working directory: ", REPO_ROOT)
println("Output directory:  ", OUTPUT_DIR)
println()

# Create package spec for current state
current_spec = PackageSpec(; name=PKG_NAME, rev=CURRENT_REV, path=REPO_ROOT)

println("Running benchmarks...")
println()

# Run benchmarks
results = AirspeedVelocity.benchmark(
    [current_spec];
    output_dir=OUTPUT_DIR,
    script=BENCH_SCRIPT,
    project_toml=BENCH_PROJECT,
    tune=false,  # Set to true for more accurate results (takes longer)
    nsamples_load_time=5,
)

println()
println("Loading results...")

# Load results
combined = AirspeedVelocity.load_results([current_spec]; input_dir=OUTPUT_DIR)

# Create markdown table
println()
println("Creating markdown table...")

table_md = AirspeedVelocity.benchpkgtable([current_spec]; input_dir=OUTPUT_DIR)

# Save to file with timestamp
timestamp = Dates.format(Dates.now(), dateformat"yyyy-mm-dd_HHMMSS")
output_file = joinpath(OUTPUT_DIR, "benchmark_current_$(timestamp).md")

open(output_file, "w") do io
    println(io, "# MacroEconometricTools.jl Benchmark Results")
    println(io)
    println(io, "**Date**: ", Dates.format(Dates.now(), dateformat"yyyy-mm-dd HH:MM:SS"))
    println(io)
    println(io, "**State**: Current working directory (dirty)")
    println(io)
    println(io, "---")
    println(io)
    println(io, table_md)
    println(io)
    println(io, "---")
    println(io, "_Generated by `benchmark/run_benchmark_current.jl`_")
end

println()
println("=" ^ 60)
println("Benchmark complete!")
println("=" ^ 60)
println("Results saved to: ", output_file)
println()
println("To view the markdown table:")
println("  cat ", output_file)
println()
